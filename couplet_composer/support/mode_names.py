"""
This support module contains the running mode name constants.
"""


def get_preset_mode_name():
    """Gives the name of the preset mode."""
    return "preset"


def get_configuring_mode_name():
    """Gives the name of the configuring mode."""
    return "configure"


def get_composing_mode_name():
    """Gives the name of the composing mode."""
    return "compose"
